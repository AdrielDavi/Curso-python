#Faça um programa em Python que receba quatro notas, 
#calcule e imprima a média aritmética.

x = int (input("Digite a primeira nota:"))
y = int (input("Digite a segunda nota:"))
z = int (input("Digite a terceira nota:"))
k = int (input("Digite a quarta nota:"))

print ("A média aritmética é ",(x+y+z+k)/4)